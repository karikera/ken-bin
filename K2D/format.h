#pragma once

#include "common.h"

enum class kr::image::PixelFormat
{
	Invalid = -1,
	Index,		// 0xII [0xII]
	A8,			// 0xAA [0xAA]
	R5G6B5,		// 0bRRRRRGGGGGGBBBBB [0bGGGBBBBB, 0bRRRRRGGG]
	X1RGB5,		// 0bXRRRRRGGGGGBBBBB [0bGGGBBBBB, 0bXRRRRRGG]
	A1RGB5,		// 0bARRRRRGGGGGBBBBB [0bGGGBBBBB, 0bARRRRRGG]
	ARGB4,		// 0xARGB [0xGB, 0xAR]
	RGB8,		// 0xRRGGBB [0xBB,0xGG,0xRR]
	XRGB8,		// 0xXXRRGGBB [0xBB,0xGG,0xRR,0xXX]
	ARGB8,		// 0xAARRGGBB [0xBB,0xGG,0xRR,0xAA]
	BGR8,		// 0xBBGGRR [0xRR,0xGG,0xBB]
	XBGR8,		// 0xXXBBGGRR [0xRR,0xGG,0xBB,0xXX]
	ABGR8,		// 0xAABBGGRR [0xRR,0xGG,0xBB,0xAA]
	RGBA32F,	// [0xRR,0xGG,0xBB,0xAA]
	Count,
};
